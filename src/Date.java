
public class Date{
	private int year;
	private int month; 
	private int day;

	public Date(int day, int month, int year){
		this.day=day;
		this.month=month;
		this.year=year;
	}
	
	int getDay(){
		return this.day;
	}
	void setDay(int day){
		this.day=day;
	}

	int getMonth(){
		return this.month;
	}
	void setMonth(int month){
		this.month=month;
	}
	
	int getYear(){
		return this.year;
	}
	void setYear(int year){
		this.year=year;
	}
	
	boolean isSameDay(Date otherDate){
		boolean result;
		if(this.day==otherDate.getDay()){
			result=true;
		}else{
			result=false;
		}return result;
	}

	boolean isSameMonth(Date otherDate){
		boolean result;
		if(this.month==otherDate.getMonth()){
			result=true;
		}else{
			result=false;
		}return result;
	}

	boolean isSameYear(Date otherDate){
		boolean result;
		if(this.year==otherDate.getYear()){
			result=true;
		}else{
			result=false;
		}return result;
	}
	 
	boolean isSame(Date otherDate){
		boolean result;
		if(isSameDay(otherDate)==true&&isSameMonth(otherDate)==true&&isSameYear(otherDate)==true){
			result=true;
		}else{
			result=false;
		}return result;
	}
}
	  
	 
